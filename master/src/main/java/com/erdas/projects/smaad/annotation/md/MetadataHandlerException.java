package com.erdas.projects.smaad.annotation.md;

public class MetadataHandlerException extends Exception {

	public MetadataHandlerException() {
		// Nothing to do
	}

	public MetadataHandlerException(String arg0) {
		super(arg0);
	}

	public MetadataHandlerException(Throwable arg0) {
		super(arg0);
	}

	public MetadataHandlerException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
